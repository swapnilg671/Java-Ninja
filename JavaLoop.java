public class JavaLoop {
    
public static void main(String args[]) 
        {
        //printing Stars 
              int no=4;
            for (int i=no;  i>0 ; i--){
                for (int j=0; j<i; j++){
         System.out.print("*");
                }
                 System.out.print("\n");
            }
//multiplication table 



        }
    }
    

